package Modelo;

/**
 *
 * @author Cris2
 */
public class Marca {
   Long id;
   String Nombre;

    public Marca(Long id, String Nombre) {
        this.id = id;
        this.Nombre = Nombre;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
   
}
